
public class Operation3 {

	public static void main(String[] args) {
		// 대입연산
		int a = 5;
		
		// 복합대입연산
		int b = 2;
		a += b; // a = a + b;
		System.out.println(a);
		a -= b; // a = a - b;
		System.out.println(a);
		a *= b;
		System.out.println(a);
		a /= b;
		System.out.println(a);
		a %= b;
		System.out.println(a);
		
		
	}

}
